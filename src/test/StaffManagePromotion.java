    private void jbtDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jbtDeleteActionPerformed
        for(int i =0;i< promoList.getNumberOfEntries();i++){
            
            if(jtfPromoID.getText().equals(promoList.getEntry(i+1).id)){
                promoList.removePromo(i+1);
                
                
            }
            
        }
        RefreshTable();
        jtfPromoID.setText("");
        jtfPromoName.setText("");
        jtfProductType.setText("");
        jtaDesc.setText("");
        
    }//GEN-LAST:event_jbtDeleteActionPerformed

    private void jtbPromotionMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jtbPromotionMouseClicked
        
        int selectedRow = jtbPromotion.getSelectedRow();
        DefaultTableModel model = (DefaultTableModel) jtbPromotion.getModel();
        jtfPromoID.setText(model.getValueAt(selectedRow,0).toString());
        jtfPromoName.setText(model.getValueAt(selectedRow,1).toString());
        jtfProductType.setText(model.getValueAt(selectedRow,2).toString());
        jtaDesc.setText(model.getValueAt(selectedRow,3).toString());
        
    }//GEN-LAST:event_jtbPromotionMouseClicked

