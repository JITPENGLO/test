     public void RefreshTable(){
        DefaultTableModel model = (DefaultTableModel) jtbPromotion.getModel();
        int countRow = model.getRowCount();
        for(int j = countRow-1;j>=0;j--){
            model.removeRow(j);
        }
        
        for(int i=0; i <promoList.getNumberOfEntries();i++){
            model.addRow(new Object[]{
            promoList.getEntry(i+1).getId(),
            promoList.getEntry(i+1).getName(),
            promoList.getEntry(i+1).getPromotionType(),
            promoList.getEntry(i+1).getPromotionDesc()});
        }
    }
     
